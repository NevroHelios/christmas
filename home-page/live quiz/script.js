let currentQuestion = 0;
let score = 0;
let attemptedQuestions = 0;
let seenQuestions = 0;
const totalQuestions = questions.length;


const questionContainer = document.getElementById("question-container");
const optionsContainer = document.getElementById("options-container");
const nextButton = document.getElementById("next-button");
const feedbackContainer = document.getElementById("feedback-container");
const scoreContainer = document.getElementById("score-container");
const scoreSpan = document.getElementById("score");
const attemptedContainer = document.getElementById("attempted-container");
const attemptedSpan = document.getElementById("attempted");
const totalContainer = document.getElementById("total-container");
const totalSpan = document.getElementById("total");
const timerContainer = document.getElementById("timer-container")




function showQuestion(question) {
    questionContainer.innerHTML = question.question;
    optionsContainer.innerHTML = "";

    question.options.forEach((option, index) => {
        const button = document.createElement("button");
        button.innerHTML = option;
        button.addEventListener("click", () => checkAnswer(option));
        optionsContainer.appendChild(button);
    });
}

function checkAnswer(selectedOption) {
    attemptedQuestions++;
    seenQuestions++;

    if (selectedOption === questions[currentQuestion].correctAnswer) {
        displayFeedback("Correct!", "green");
        score++; // Increment the score for correct answers
    } else {
        displayFeedback("Incorrect!", "red");
    }

    // Update and display the score
    scoreSpan.innerHTML = score;
    attemptedSpan.innerHTML = attemptedQuestions;
    totalSpan.innerHTML = seenQuestions;


    // Delay the loading of the next question by 2 seconds
    setTimeout(function() {
        currentQuestion++;

        if (currentQuestion < questions.length) {
            showQuestion(questions[currentQuestion]);
        } else {
            displayFeedback("Quiz completed!", "blue");
        }
    }, 2000);
}

function displayFeedback(message, color) {
    feedbackContainer.innerHTML = message;
    feedbackContainer.style.color = color;

    // Clear the feedback after a short delay (e.g., 2 seconds)
    setTimeout(() => {
        feedbackContainer.innerHTML = "";
    }, 1000);
}


nextButton.addEventListener("click", () => {
    seenQuestions++;
    totalSpan.innerHTML = seenQuestions;

    if (currentQuestion < questions.length) {
        showQuestion(questions[++currentQuestion]);
    }
});

document.addEventListener("DOMContentLoaded", function() {
    const snowfallContainer = document.getElementById("snowfall-container");

    function createSnowflake() {
        const snowflake = document.createElement("div");
        snowflake.className = "snowflake";
        snowfallContainer.appendChild(snowflake);

        const startPositionX = Math.random() * window.innerWidth;
        const startPositionY = -10;
        const speed = Math.random() * 2 + 1;

        snowflake.style.left = startPositionX + "px";
        snowflake.style.top = startPositionY + "px";

        animateSnowflake(snowflake, speed);
    }

    function animateSnowflake(snowflake, speed) {
        const updateInterval = 30;

        function moveSnowflake() {
            const currentTop = parseInt(snowflake.style.top, 10);
            if (currentTop < window.innerHeight) {
                snowflake.style.top = currentTop + speed + "px";
            } else {
                snowflake.style.top = "-10px";
                snowflake.style.left = Math.random() * window.innerWidth + "px";
            }
        }

        setInterval(moveSnowflake, updateInterval);
    }

    // Create initial set of snowflakes
    for (let i = 0; i < 50; i++) {
        createSnowflake();
    }

    // Create additional snowflakes every 2 seconds
    setInterval(createSnowflake, 2000);
});


function endQuiz() {
    clearInterval(timerInterval);
    displayFeedback("Quiz completed!", "blue");
    nextButton.disabled = true;
}


function startTimer(durationInSeconds) {
    let timer = durationInSeconds;
    timerInterval = setInterval(function() {
        const minutes = Math.floor(timer / 60);
        const seconds = timer % 60;

        timerContainer.innerHTML = `${minutes}:${seconds}`;

        if (--timer < 0) {
            endQuiz();
        }
    }, 1000);
}


// Initial display
showQuestion(questions[currentQuestion]);
startTimer(300);